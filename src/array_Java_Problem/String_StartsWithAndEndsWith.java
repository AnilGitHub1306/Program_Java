package array_Java_Problem;

public class String_StartsWithAndEndsWith {

	public static void main(String[] args) {
		
		String a = "rahulkumar";
		
		System.out.println(a.startsWith("rah"));	
		System.out.println(a.startsWith("ra"));		
		System.out.println(a.startsWith("mar"));
		
		System.out.println(a.endsWith("mar"));
		System.out.println(a.endsWith("umar"));
		System.out.println(a.endsWith("MAR"));
		


	}

}
